#include <stdio.h>

int main(){
    int a = 5;

    //Pre-incremento 
    /*
    printf("%d \n", ++a);
    printf("%d \n", ++a);
    printf("%d \n", ++a);
    */

    //Post-incremento 
    /*
    printf("%d \n", a++);
    printf("%d \n", a++);
    printf("%d \n", a++);
    printf("%d \n", a);
    */

   //Pre-decremento 
   printf("%d \n", --a);
   printf("%d \n", --a);
   printf("%d \n", --a);

   //Post-decremento 
   printf("%d \n", a--);
   printf("%d \n", a--);
   printf("%d \n", a--);
   printf("%d \n", a);


    return 0;
}